    public void printLaberinto(Graphics g){
        int f,c,x=0,y=0;   
      
        for(f=0;f<13;f++){
                for(c=0;c<25;c++){
                    x=c*50;
                    y=f*50;        
                    if(matPacMan[f][c]==0){
                        g.setColor(Color.GRAY);
                        g.fillRect(x, y, 50, 50);
                      
                    } else if(matPacMan[f][c]==1){
                        
                        g.setColor(Color.red);
                        g.fillOval(x+20, y+20, 10, 10);
                    
                    }else if(matPacMan[f][c]==4){
                        
                        g.setColor(Color.cyan);
                        g.fillOval(x+20, y+20, 20, 20);
                     
                    }else{
                        
                        g.setColor(Color.black);
                        g.fillOval(x+20, y+20, 21, 21);
                    }
                }
        
        }
       
  
    }
 
